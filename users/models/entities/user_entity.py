class User:
    def __init__(self, id, username, password, role, cuit, name, address, phone, mobile, contact, email):
        self.id = id
        self.username = username
        self.password = password
        self.role = role
        self.cuit = cuit
        self.name = name
        self.address = address
        self.phone = phone
        self.mobile = mobile
        self.contact = contact
        self.email = email

